#include<bits/stdc++.h>

using namespace std;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int tt;
    cin>>tt;
    while(tt--){
        int n;
        cin>>n;
        for(int i=n; i>=1; i--)cout<<i<<" ";
        cout<<endl;
    }
    return 0;
}